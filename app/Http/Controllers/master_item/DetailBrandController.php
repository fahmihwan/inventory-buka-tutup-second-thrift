<?php

namespace App\Http\Controllers\master_item;

use App\Http\Controllers\Controller;

use App\Models\Detail_brand;

use Illuminate\Http\Request;

class DetailBrandController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $detail_brand = Detail_brand::all();
        return view('pages.master_item.detail_brand.index', [
            'detail_brand' => $detail_brand
        ]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $data = $request->validate([
            'name' => 'required|unique:detail_brands'
        ]);

        Detail_brand::create($data);
        return redirect('/master/detail-brand');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        return view('pages.master_item.detail_brand.edit', [
            'data' => Detail_brand::where('id', $id)->first()
        ]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $data = $request->validate([
            'name' => 'required|unique:category_brands',
        ]);

        Detail_brand::where('id', $id)->update($data);
        return redirect('/master/detail-brand');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Detail_brand::where('id', $id)->delete();
        return redirect('/master/detail-brand');
    }
}
